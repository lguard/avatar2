#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	size;
	int		i;

	size = ft_strlen(s1);
	i = 0;
	while (s2[i])
	{
		s1[size + i] = s2[i];
		i++;
	}
	s1[size + i] = '\0';
	return (s1);
}
